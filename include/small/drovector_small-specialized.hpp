//=============================================================================
/*! calculate vector product only for 3D vector */
inline drovec3 operator/(const drovec3& A, const drovec3& B)
{VERBOSE_REPORT;
  drovec3 C;
  C(0) =A(1)*B(2) -A(2)*B(1);
  C(1) =A(2)*B(0) -A(0)*B(2);
  C(2) =A(0)*B(1) -A(1)*B(0);
  return C;
}
